                      </a>
                    </h4>
                  </div>
                  <div id="collapse4" class="panel-collapse collapse">
                        <div class="panel-body">
                            This is the exchange rate that calibrates prices from different countries to a common 
                            frame of reference by adjusting for the cost of living.  
                            While there are several different sets of PPP exchange rates in the world, 
                            the ones used in our site are those calculated by the International Monetary Fund (IMF).  
                            <br /><br />
                            PPP rates are more stable than spot exchange rates which fluctuate daily, 
                            and have the potential to drop or rise for reasons unrelated to the cost of living, 
                            such as trade wars or capital flight.  
                            <br /><br />
                            However, the use of PPP exchange rates is still a matter of some debate!  
                            That is why on our detail page we include native currency references as well. 
