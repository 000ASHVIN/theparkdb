                      </a>
                    </h4>
                  </div>
                  <div id="collapse3" class="panel-collapse collapse">
                        <div class="panel-body">
                            We use comparables in two ways.  
                            <br /><br />
                            The stock market actually assigns an enterprise value for the publicly traded theme park chains every day.  
                            This is the case with Cedar Fair, Disney, and so on.  
                            We assume that the value of a Disney park is likely not that different on any given Wednesday compared to Tuesday, 
                            so our values are average annual values based on the average annual value of the park during the preceding year.  
                            <br /><br />
                            The second method is that every so often, 
                            theme parks are bought and sold.  These deals reveal transaction 
                            multiples that we then apply to parks of a similar nature. 
                        </div>
                    </div>
                </div>
                
